export type ServiceCardPresetVariant = "development" | "consulting";

export function getServiceCardPresetReply(
  variant: ServiceCardPresetVariant,
  label: string
): string | undefined {
  if (variant === "development") {
    return DEVELOPMENT_PRESET_REPLIES[label as DevelopmentPresetLabel];
  }
  return CONSULTING_PRESET_REPLIES[label as ConsultingPresetLabel];
}

if (process.env.NODE_ENV === "development") {
  for (const label of DEVELOPMENT_PRESET_LABELS) {
    const text = DEVELOPMENT_PRESET_REPLIES[label];
    const n = text.length;
    console.assert(
      n >= 400 && n <= 750,
      `[service-card-preset] development "${label}" length ${n} (expected ~450–550+)`
    );
  }
  for (const label of CONSULTING_PRESET_LABELS) {
    const text = CONSULTING_PRESET_REPLIES[label];
    const n = text.length;
    console.assert(
      n >= 400 && n <= 750,
      `[service-card-preset] consulting "${label}" length ${n} (expected ~450–550+)`
    );
  }
}
